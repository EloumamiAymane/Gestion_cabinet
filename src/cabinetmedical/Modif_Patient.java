    void updatePatient(int id,String Name,String Prenom,int age,String Adresse,String Sexe,String Tel) throws Exception{
         PreparedStatement stm=c.prepareStatement("Update patient set NOM_PAT=?,PRENOM_PAT=?,AGE_PAT=?,ADRESSE_PAT=?,SEXE_PAT=?,TEL_PAT=? where id_pat=?");
         stm.setString(1, Name);
        stm.setString(2, Prenom);
        stm.setInt(3, age);
        stm.setString(4, Adresse);
        stm.setString(5, Sexe);
        stm.setString(6, Tel);
         stm.setInt(7, id);
        stm.executeUpdate();
    }






