    void Ajouter(int idOr,int idMedcm,int Quantite,int duree) throws Exception{
        PreparedStatement p=c.prepareStatement("insert into prescrire values(?,?,?,?)");
        p.setInt(1, idOr);
          p.setInt(2, idMedcm);
            p.setInt(3, Quantite);
              p.setInt(4, duree);
              p.executeUpdate();
    }
    
    void AfficherIDOR() {
        try{
        id_Ord.removeAllItems();
        Statement s=c.createStatement();
        ResultSet res=s.executeQuery("select id_ord from ordonnance");
        while(res.next()){
           int idOrd=res.getInt(1);
            String idO=  Integer.toString(idOrd);
            id_Ord.addItem(idO);
            
        }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
       void AfficherIDMED() {
        try{
        id_Med.removeAllItems();
        Statement s=c.createStatement();
        ResultSet res=s.executeQuery("select id_medm,nom_medm from medicaments");
        while(res.next()){
           int idOrd=res.getInt(1);
           String nom=res.getString(2);
            String idO=  Integer.toString(idOrd)+"-"+nom;
            id_Med.addItem(idO);
        }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
