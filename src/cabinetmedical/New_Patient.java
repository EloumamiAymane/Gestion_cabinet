    private void ADDPATIENTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDPATIENTActionPerformed
  if(Nom.getText().equals("")||this.Prenom.getText().equals("")||this.Adresse.getText().equals("")||
              GenreM.getText().equals("")||GenreF.getText().equals("")||this.Tel.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Tout les Infos sont obligatoire !");
      }
      else{
        try{
        String name=Nom.getText();
        String Prenom=this.Prenom.getText();
        int age=Integer.parseInt(Age.getText());
        String Adresse=this.Adresse.getText();
        String Sexe="";
        if(GenreM.isSelected()){
            Sexe=GenreM.getText();
        }
        if(GenreF.isSelected()){
            Sexe=GenreF.getText();
        }
      
        String Tel="+212"+this.Tel.getText();
       
     
            AjouterPatient(name,Prenom,age,Adresse,Sexe,Tel);
            JOptionPane.showMessageDialog(this,"Le patient est ajouté avec succés !");
     
 
        }catch(Exception e){
          JOptionPane.showMessageDialog(this,"Erreur:"+e.getMessage());
        }
      }
      // dispose();
    }//GEN-LAST:event_ADDPATIENTActionPerformed

