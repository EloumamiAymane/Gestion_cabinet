    private void deleteRDVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteRDVActionPerformed
           //supprimer par selection
            int z=0;
      int []indices=  tableRDV.getSelectedRows();
     
     if(indices.length!=0){
          int x=JOptionPane.showConfirmDialog(this, "Etes vous sur de vouloir faire la suppression !");
         try{
             
         if(x==0){
      for(int i=0;i<indices.length;i++){
         int numeroSelectionner=(Integer)tableRDV.getValueAt(indices[i], 0);
          SupprimerRDV(numeroSelectionner);
          z++;
     
      }
         }
     
        }catch(Exception e){
             JOptionPane.showMessageDialog(this, "erreur de saisie!");
             e.printStackTrace();
        } 
         if(z==1){
              JOptionPane.showMessageDialog(this, "Rendez vous a été supprimé !");
         }
         if(z>1){
              JOptionPane.showMessageDialog(this, " Rendez vous ont été supprimé !");
         }
     }else{
          JOptionPane.showMessageDialog(this, "Erreur  please select a Row !");
     }
    }//GEN-LAST:event_deleteRDVActionPerformed

    private void AnnulerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnnulerActionPerformed
       int x= tableRDV.getSelectedRow();
    
     if(x!=-1){
        
         int numeroSelectionner=(Integer)tableRDV.getValueAt(x, 0);
        // System.out.println(numeroSelectionner);
          try {
              //p.show();
                PreparedStatement stm = c.prepareStatement("update rendez_vous set etat='Annuler' where id_rdv=?");
              stm.setInt(1, numeroSelectionner);
            stm.executeUpdate();
            afficherRDV();
       
          } catch (Exception e) {
              JOptionPane.showMessageDialog(this, "Erreur !"+e.getMessage());
          }
         
     
      }else{
          JOptionPane.showMessageDialog(this, "Erreur  please select a Row !");
     }    
    }//GEN-LAST:event_AnnulerActionPerformed

