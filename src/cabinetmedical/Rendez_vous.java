      void afficherRDV() throws Exception {
          
           DefaultTableModel  z=new DefaultTableModel(null,
                   new String[]{"ID_RDV","Prenom","Nom","Date_Present","Heure_Present","Remarque","Status"});
       
		Statement stmt =c.createStatement();
	ResultSet rslt=stmt.executeQuery("select r.id_rdv,p.prenom_pat,p.nom_pat,r.date_rdv,r.heure_rdv,r.remarque,r.etat from patient p, rendez_vous r where p.id_pat=r.id_pat");
		//ResultSet rslt=stmt.executeQuery("select * from rendez_vous");
		while(rslt.next()) {
                   int id=rslt.getInt(1);
                    
                   String prenom=rslt.getString(2);
                   String nom=rslt.getString(3);
                   Date d=rslt.getDate(4);
                   String heure=rslt.getString(5);
                   String remarque=rslt.getString(6);
                   String status=rslt.getString(7);
                   // System.out.println(remarque);
	z.addRow(new Object[]{id,prenom,nom,d,heure,remarque,status});
                      
		}
               
               
                tableRDV.setModel(z);
               // System.out.println(tableRDV);
                

               
    
       } 
    
