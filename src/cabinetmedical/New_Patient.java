    void AjouterPatient(String Name,String Prenom,int age,String Adresse,String Sexe,String Tel) throws Exception{
        PreparedStatement stm=c.prepareStatement("Insert into patient(NOM_PAT,PRENOM_PAT,AGE_PAT,ADRESSE_PAT,SEXE_PAT,TEL_PAT,taille,poids,tension,temperature,antecedents) values(?,?,?,?,?,?,?,?,?,?,?)");
        stm.setString(1, Name);
        stm.setString(2, Prenom);
        stm.setInt(3, age);
        stm.setString(4, Adresse);
        stm.setString(5, Sexe);
        stm.setString(6, Tel);
        stm.setFloat(7, 0);
        stm.setFloat(8, 0);
        stm.setFloat(9, 0);
        stm.setFloat(10, 0);
         stm.setString(11, "");
        stm.executeUpdate();
       
    }
