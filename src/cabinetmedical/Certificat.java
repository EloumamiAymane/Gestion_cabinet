    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
     
       if(this.id.getText().equals("")|| this.NbrJour.getText().equals("") ||this.detail.getText().equals("")){
          JOptionPane.showMessageDialog(this, "Toutes les infos sont obligatoires ! ");
      }
       else{
           
            int id=Integer.parseInt(this.id.getText());
       int nbr=Integer.parseInt(
               NbrJour.getText());
       String detail=this.detail.getText();
       try{
           Inserer(id, nbr,detail);
            JOptionPane.showMessageDialog(this, "Ajoute avec succes ! ");
       }catch(Exception e){
           JOptionPane.showMessageDialog(this, "Erreur "+e.getMessage());
       }
       }
    }//GEN-LAST:event_SaveActionPerformed

