      void Inserer(int id,int nbr, String details) throws Exception{
       
        PreparedStatement p=c.prepareStatement("Insert into certificat_medical values(?,?,?)");
        p.setInt(1, id);
         p.setInt(2, nbr);
        p.setString(3,details);
        p.executeUpdate();
    }
    

