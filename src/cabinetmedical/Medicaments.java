    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
         if(this.id.getText().equals("")|| this.nom.getText().equals("")
             ||   this.dose.getText().equals("")|| this.description.getText().equals("")
                 ){
          JOptionPane.showMessageDialog(this, "Toutes les infos sont obligatoires ! ");
      }
      else{
       try{
           int id=Integer.parseInt(this.id.getText());
           String nom=this.nom.getText();
           String dose= this.dose.getText();
           String desc=this.description.getText();
           Inserer(id, nom,dose,desc);
            JOptionPane.showMessageDialog(this, "Ajoute avec succes ! ");
       }catch(Exception e){
           JOptionPane.showMessageDialog(this, "Erreur "+e.getMessage());
       }
      }
        
        
    }//GEN-LAST:event_saveActionPerformed

