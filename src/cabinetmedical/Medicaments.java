    void Inserer(int id,String nom,String dose,String desc) throws Exception{
        
        PreparedStatement p=c.prepareStatement("Insert into medicaments values(?,?,?,?)");
        p.setInt(1, id);
        p.setString(2, nom);
        p.setString(3, dose);
        p.setString(4, desc);
        
        p.executeUpdate();
    }

