    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
     
        String str=id_Ord.getSelectedItem().toString();
         String str1=id_Med.getSelectedItem().toString();
         String s=str1.substring(0, 1);
         
         int idO=Integer.parseInt(str);
         int idM=Integer.parseInt(s);
         if(qte.getText().equals("")||duree.getText().equals("")){
             JOptionPane.showMessageDialog(this, "Toutes les infos sont obligatoires ");
         }
         else{
             try{
             int qte=Integer.parseInt(this.qte.getText());
             int duree=Integer.parseInt(this.duree.getText());
                 Ajouter(idO, idM, qte, duree);
                  JOptionPane.showMessageDialog(this, "Ajout avec succes !");
             }catch(Exception e){
                 JOptionPane.showMessageDialog(this, "Erreur "+e.getMessage());
             }
         }
        
        
        
    }//GEN-LAST:event_saveActionPerformed

