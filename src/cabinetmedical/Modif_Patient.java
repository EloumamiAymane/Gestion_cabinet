    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
         if(NomM.getText().equals("")||this.PrenomM.getText().equals("")||this.AdresseM.getText().equals("")||
              GenreMo.getText().equals("")||GenreFM.getText().equals("")||this.TelM.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Tout les Infos sont obligatoire !");
      }
         else{
               try{
        String id=this.id.getText();
        int idP=Integer.parseInt(id);
        String name=NomM.getText();
        String Prenom=this.PrenomM.getText();
        int age=Integer.parseInt(AgeM.getText());
        String Adresse=this.AdresseM.getText();
        String Sexe="";
        if(GenreMo.isSelected()){
            Sexe=GenreMo.getText();
        }
        if(GenreFM.isSelected()){
            Sexe=GenreFM.getText();
        }
      
        String Tel=this.TelM.getText();
       
                   updatePatient(idP, name, Prenom, age, Adresse, Sexe, Tel);
           
            JOptionPane.showMessageDialog(this,"Le patient est mis a jour  avec succés !");
     
 
        }catch(Exception e){
          JOptionPane.showMessageDialog(this,"Erreur:"+e.getMessage());
        }
      // dispose();
         }
    }//GEN-LAST:event_UpdateActionPerformed

